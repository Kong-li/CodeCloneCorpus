function enqueue(que, element) {
  let pos = que.length;
  que.push(element);
  for (let i = pos; ; ) {
    const parentIndex = Math.floor((i - 1) / 2),
          parentNode = que[parentIndex];
    if (!compare(parentNode, element)) break;
    [que[i], que[parentIndex]] = [que[parentIndex], que[i]];
    i = parentIndex;
  }
}

